list1 = [1, 2, 3, 4, 5]
# insert() method inserts an element to the list at a specified index
# index starts from 0
#The fisrt argument is the index and the second is the value.
#3rd index means at 4th position.
#0th index means at 1st position.
list1.insert(3, 6)
print(list1) # Output: [1, 2, 3, 6, 4, 5]


